def generate_ci_fix(cfg: Config) -> GeneratedWorkflow:
    wf = cfg.workflows.get("ci-fix", WorkflowConfig())
    if wf.watched_workflows is None:
        raise click.ClickException(
            "ci-fix requires watched_workflows — specify the CI workflow(s) to monitor.\n\n"
            "  workflows:\n"
            "    ci-fix:\n"
            '      watched_workflows: ["ci"]'
        )
    watched = wf.watched_workflows
    branches = wf.branches if wf.branches is not None else [cfg.default_branch]
    prompt = (wf.prompt or _default_prompt(cfg, "ci-fix", "{run_id}")).replace(
        "{run_id}", "${{ github.event.workflow_run.id }}"
    )
    bt = _bot_token(cfg)

    setup = _setup_yaml(cfg)
    perms = _permissions(issues=False)
    watched_yaml = ", ".join(f'"{w}"' for w in watched)
    branches_yaml = ", ".join(f'"{b}"' for b in branches)
    conclusion_check = "github.event.workflow_run.conclusion == 'failure'"
    guard = _fork_guard(cfg)
    guard_if = f"{guard} && {conclusion_check}" if guard else conclusion_check
    agent = _agent_step(
        cfg,
        f"prompt: |\n            {prompt}\n"
        f"            - Run URL: ${{{{ github.event.workflow_run.html_url }}}}\n"
        f"            - Commit: ${{{{ github.event.workflow_run.head_sha }}}}\n"
        f"            - Commit message: ${{{{ github.event.workflow_run.head_commit.message }}}}",
    )

    content = f"""\
{HEADER}
name: tend-ci-fix
on:
  workflow_run:
    workflows: [{watched_yaml}]
    types: [completed]
    branches: [{branches_yaml}]

jobs:
  fix-ci:
    if: {guard_if}
    runs-on: ubuntu-24.04
    permissions:
{perms}
    steps:
      - uses: actions/checkout@v6
        with:
          ref: {cfg.default_branch}
          fetch-depth: 0
          fetch-tags: true
          token: {bt}
{setup}
{agent}
"""
    return GeneratedWorkflow(filename="tend-ci-fix.yaml", content=content)


