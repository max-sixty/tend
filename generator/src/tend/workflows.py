"""Workflow definitions and YAML rendering."""

from __future__ import annotations

from collections.abc import Callable
from dataclasses import dataclass

from tend.config import Config, WorkflowConfig

HEADER = "# Generated by tend. Regenerate with: uvx tend init\n"


def _bot_token(cfg: Config) -> str:
    return f"${{{{ secrets.{cfg.bot_token_secret} }}}}"


def _claude_token(cfg: Config) -> str:
    return f"${{{{ secrets.{cfg.claude_token_secret} }}}}"


def _setup_yaml(cfg: Config, indent: int = 6) -> str:
    """Render setup steps as YAML, indented to `indent` spaces.

    Returns empty string when no steps, or newline-prefixed block when present,
    so templates can write `{setup}` without extra blank lines.
    """
    pad = " " * indent
    lines = []
    for step in cfg.setup:
        if step.uses:
            lines.append(f"{pad}- uses: {step.uses}")
        elif step.run:
            lines.append(f"{pad}- run: {step.run}")
    if not lines:
        return ""
    return "\n" + "\n".join(lines) + "\n"


def _permissions(issues: bool = True) -> str:
    lines = [
        "      contents: write",
        "      pull-requests: write",
        "      id-token: write",
        "      actions: read",
    ]
    if issues:
        lines.append("      issues: write")
    return "\n".join(lines)


def _escape(s: str) -> str:
    """Escape single quotes for GitHub Actions expressions."""
    return s.replace("'", "''")


@dataclass
class GeneratedWorkflow:
    filename: str
    content: str


