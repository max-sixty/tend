_SCHEDULED_TMPL = _JINJA.from_string("""\
<<header>>
name: tend-<<name>>
on:
  schedule:
    - cron: "<<cron>>"
  workflow_dispatch:

jobs:
  <<name>>:
<<fork_guard_if(cfg)>>    runs-on: ubuntu-24.04
    permissions:
<<permissions()>>
    steps:
<<checkout(cfg, ref=cfg.default_branch)>>
<<setup>>
<<agent_step(cfg, block_prompt(prompt))>>
""")


# Default cron times target Anthropic's off-peak hours (outside weekday
# 8am–2pm ET / 12:00–18:00 UTC). Non-round minutes avoid thundering herd.
_SCHEDULED_DEFAULT_CRON = {
    "nightly": "17 6 * * *",
    "weekly": "17 9 * * 0",
    "review-runs": "47 7 * * *",
}


def _generate_scheduled(cfg: Config, name: str) -> GeneratedWorkflow:
    wf = cfg.workflows.get(name, WorkflowConfig())
    cron = wf.cron or _SCHEDULED_DEFAULT_CRON[name]
    prompt = wf.prompt or cfg.default_prompt(name)

    content = _SCHEDULED_TMPL.render(
        cfg=cfg,
        name=name,
        cron=cron,
        setup=_setup_yaml(cfg),
        prompt=prompt,
    )
    return GeneratedWorkflow(filename=f"tend-{name}.yaml", content=content)


_NOTIFICATIONS_TMPL = _JINJA.from_string("""\
<<header>>
name: tend-notifications
on:
  schedule:
    - cron: "<<cron>>"
  workflow_dispatch:

jobs:
  notifications:
<<fork_guard_if(cfg)>>    runs-on: ubuntu-24.04
    permissions:
<<permissions()>>
    steps:
      - name: Check for unread notifications
        id: check
        run: |
          COUNT=$(gh api notifications --jq 'length')
          if [ "$COUNT" = "0" ]; then
            echo "count=0" >> "$GITHUB_OUTPUT"
            echo "No unread notifications — skipping"
            exit 0
          fi

          # --- Layer B: drop notifications shadowed by recent dedicated runs ---
          # Event workflows mark their own notifications read via action.yaml's
          # post-step on success; this sweeps the case where Claude failed
          # (post-step is gated by `if: success()`) so the notification still
          # gets cleared without burning Claude turns to rediscover it.
          SINCE=$(date -u -d '30 minutes ago' +%Y-%m-%dT%H:%M:%SZ)
          RECENT_PRS=$(gh api "repos/$GITHUB_REPOSITORY/actions/runs?created=>=$SINCE&per_page=50" --jq '[.workflow_runs[] | select(.name | test("^(tend-review|tend-mention|tend-triage|tend-ci-fix)$")) | .pull_requests[]?.number] | unique | .[]' || true)

          if [ -n "$RECENT_PRS" ]; then
            NOTIFS=$(gh api notifications)
            for pr in $RECENT_PRS; do
              echo "$NOTIFS" | jq -r --arg repo "$GITHUB_REPOSITORY" --arg pr "$pr" '.[] | select(.subject.url == "https://api.github.com/repos/" + $repo + "/pulls/" + $pr or .subject.url == "https://api.github.com/repos/" + $repo + "/issues/" + $pr) | .id' | while read -r tid; do
                [ -n "$tid" ] || continue
                gh api "notifications/threads/$tid" -X PATCH || true
              done
            done
          fi

          # --- Layer C: drop notifications on bot-authored closed PRs ---
          # The bot auto-subscribes to its own PRs. After merge/close, leftover
          # subscription notifications are pure noise — no action needed.
          NOTIFS=$(gh api notifications)
          echo "$NOTIFS" | jq -r --arg repo "$GITHUB_REPOSITORY" '.[] | select(.repository.full_name == $repo and .subject.type == "PullRequest") | .id' | while read -r tid; do
            [ -n "$tid" ] || continue
            PR_NUM=$(echo "$NOTIFS" | jq -r --arg tid "$tid" '.[] | select(.id == $tid) | .subject.url | split("/") | last')
            PR_INFO=$(gh api "repos/$GITHUB_REPOSITORY/pulls/$PR_NUM" --jq '"\\(.user.login) \\(.state)"' 2>/dev/null) || continue
            PR_AUTHOR=${PR_INFO%% *}
            PR_STATE=${PR_INFO##* }
            if [ "$PR_AUTHOR" = "<<cfg.bot_name>>" ] && [ "$PR_STATE" = "closed" ]; then
              gh api "notifications/threads/$tid" -X PATCH || true
            fi
          done

          # --- Layer D: count processable notifications ---
          # Same-repo notifications younger than 10 minutes are deferred: a
          # dedicated workflow (tend-review/mention/triage/ci-fix) is likely
          # still starting up or mid-flight and hasn't posted its response yet.
          # Processing them now risks duplicating work. Cross-repo notifications
          # are exempt — no dedicated workflow handles them.
          CUTOFF=$(date -u -d '10 minutes ago' +%Y-%m-%dT%H:%M:%SZ)
          REMAINING=$(gh api notifications)
          COUNT=$(echo "$REMAINING" | jq --arg repo "$GITHUB_REPOSITORY" --arg cutoff "$CUTOFF" '[.[] | select(.repository.full_name != $repo or .updated_at <= $cutoff)] | length')
          echo "count=$COUNT" >> "$GITHUB_OUTPUT"
          if [ "$COUNT" = "0" ]; then
            TOTAL=$(echo "$REMAINING" | jq 'length')
            if [ "$TOTAL" = "0" ]; then
              echo "All notifications handled by pre-checks — skipping"
            else
              echo "$TOTAL notification(s) remain but all are fresh same-repo (deferred) — skipping"
            fi
          else
            echo "$COUNT processable notification(s) — proceeding"
          fi
        env:
          GITHUB_TOKEN: ${{ secrets.<<cfg.bot_token_secret>> }}

<<checkout(cfg, ref=cfg.default_branch, if_condition=skip_condition)>>
<<setup>><<agent_step(cfg, block_prompt(prompt), if_condition=skip_condition)>>
""")


def generate_notifications(cfg: Config) -> GeneratedWorkflow:
    wf = cfg.workflows.get("notifications", WorkflowConfig())
    cron = wf.cron or "*/15 * * * *"
    prompt = wf.prompt or cfg.default_prompt("notifications")

    skip_condition = (
        "steps.check.outputs.count != '0' || github.event_name == 'workflow_dispatch'"
    )

    content = _NOTIFICATIONS_TMPL.render(
        cfg=cfg,
        cron=cron,
        skip_condition=skip_condition,
        setup=_setup_yaml(cfg, condition=skip_condition),
        prompt=prompt,
    )
    return GeneratedWorkflow(filename="tend-notifications.yaml", content=content)


# ---------------------------------------------------------------------------
# Extras (workflow_extra / jobs pass-through)
# ---------------------------------------------------------------------------


def _deep_merge(base: dict, override: dict) -> dict:
    """RFC 7396 JSON Merge Patch: mappings deep-merge, scalars/lists replace.

    `base` is deep-copied so ruamel.yaml `CommentedMap` style metadata
    (key order, scalar quoting, sequence indentation) survives the merge —
    without this, `_apply_extras`-applied workflows lose all rendering
    style on every regen, even for keys the user didn't override.
    """
    result = copy.deepcopy(base)
    for key, value in override.items():
        if value is None:
            result.pop(key, None)
        elif isinstance(value, dict) and isinstance(result.get(key), dict):
            result[key] = _deep_merge(result[key], value)
        else:
            result[key] = value
    return result


def _apply_extras(wf: GeneratedWorkflow, wf_cfg: WorkflowConfig) -> GeneratedWorkflow:
    """Merge workflow_extra and per-job extras into rendered YAML (RFC 7396)."""
    if not wf_cfg.workflow_extra and not wf_cfg.jobs:
        return wf

    data = _YAML_BLOCK.load(wf.content)

    if wf_cfg.workflow_extra:
        data = _deep_merge(data, wf_cfg.workflow_extra)

    if wf_cfg.jobs:
        jobs = data.get("jobs", {})
        for job_name, job_extra in wf_cfg.jobs.items():
            if job_name in jobs:
                jobs[job_name] = _deep_merge(jobs[job_name], job_extra)
            else:
                click.echo(
                    f"Warning: job '{job_name}' not found in {wf.filename} "
                    f"(known: {', '.join(sorted(jobs))})",
                    err=True,
                )

    # `typ="rt"` preserves the leading HEADER comment block on load, so it
    # round-trips into the dumped output. Don't prepend HEADER manually
    # here or it appears twice.
    buf = io.StringIO()
    _YAML_BLOCK.dump(data, buf)
    return GeneratedWorkflow(filename=wf.filename, content=buf.getvalue())


# ---------------------------------------------------------------------------
# Install test (one-shot, generated only with --with-install-test)
# ---------------------------------------------------------------------------


def generate_install_test(cfg: Config) -> GeneratedWorkflow:
    """One-shot pre-merge verification of an install PR.

    Generated only when `tend init --with-install-test` is passed. Lives on
    the install branch, fires on the install PR via `pull_request` (which
    uses the PR head's workflow file, not the default branch's), then
    disappears from the default branch on the first nightly regen — which
    runs `tend init` without the flag, and the init cleanup step removes
    any stale tend-*.yaml files.

    Verifies what can be checked without admin token or harness invocation:
    bot+harness secrets exist on the repo, and the committed workflow
    files match what the generator would produce now. Harness auth is
    exercised end-to-end by `tend-review` on the first post-merge PR.
    """
    bt_name = cfg.bot_token_secret
    if cfg.harness == "claude":
        harness_envs = (
            f"          CLAUDE_OAUTH: ${{{{ secrets.{cfg.claude_token_secret} }}}}\n"
            f"          ANTHROPIC_KEY: ${{{{ secrets.{cfg.anthropic_api_key_secret} }}}}"
        )
        harness_names = f"{cfg.claude_token_secret} or {cfg.anthropic_api_key_secret}"
        harness_check = '"$CLAUDE_OAUTH$ANTHROPIC_KEY"'
    else:
        harness_envs = (
            f"          CODEX_AUTH: ${{{{ secrets.{cfg.codex_auth_json_secret} }}}}\n"
            f"          OPENAI_KEY: ${{{{ secrets.{cfg.openai_key_secret} }}}}"
        )
        harness_names = f"{cfg.codex_auth_json_secret} or {cfg.openai_key_secret}"
        harness_check = '"$CODEX_AUTH$OPENAI_KEY"'

    content = f"""\
{HEADER}
name: tend-install-test
on:
  pull_request:
    paths:
      - .github/workflows/tend-*.yaml
      - .config/tend.yaml

jobs:
  install-test:
    # Same-repo PRs only. Fork PRs don't carry secrets and the workflow
    # is short-lived (removed on the next nightly regen), so cross-fork
    # validation isn't worth special-casing.
    if: github.event.pull_request.head.repo.full_name == github.repository
    runs-on: ubuntu-24.04
    permissions:
      contents: read
    steps:
      - uses: actions/checkout@v6
      - uses: astral-sh/setup-uv@v6
      - name: Verify required secrets are set
        env:
          BOT_TOKEN: ${{{{ secrets.{bt_name} }}}}
{harness_envs}
        run: |
          missing=""
          [ -n "$BOT_TOKEN" ] || missing="$missing {bt_name}"
          if [ -z {harness_check} ]; then
            missing="$missing harness-auth(set {harness_names})"
          fi
          if [ -n "$missing" ]; then
            echo "::error::Missing repo secrets:$missing"
            exit 1
          fi
      - name: Verify generator output matches committed files
        env:
          GH_TOKEN: ${{{{ github.token }}}}
        run: |
          # `actions/checkout` doesn't set `origin/HEAD`, so the generator's
          # default-branch probe falls back to "main" and a repo with a
          # non-`main` default reports spurious drift. Query the remote.
          git remote set-head origin --auto
          # Pin the regen to the version in the committed header so a tend
          # release between local `init` and this CI run doesn't fail the
          # drift check for an irrelevant reason.
          HEADER=$(head -1 .github/workflows/tend-install-test.yaml)
          HEADER="${{HEADER#'# Generated by tend '}}"
          TEND_VERSION="${{HEADER%%'. Regenerate'*}}"
          uvx "tend@$TEND_VERSION" init --with-install-test
          if ! git diff --quiet .github/workflows/; then
            echo "::error::Committed workflows differ from 'uvx tend@$TEND_VERSION init --with-install-test' output. Run it locally and commit the result."
            git --no-pager diff .github/workflows/
            exit 1
          fi
"""
    return GeneratedWorkflow(filename="tend-install-test.yaml", content=content)


