def _escape_braces(prompt: str, placeholder: str) -> tuple[str, bool]:
    """Escape literal braces in a prompt, preserving only {placeholder}.

    Returns (escaped_prompt, needs_format). In the escaped prompt, {placeholder}
    is replaced with {0} for use with GitHub Actions format(), and all other
    braces are doubled to prevent format() from interpreting them.
    """
    sentinel = "\x00PLACEHOLDER\x00"
    text = prompt.replace(f"{{{placeholder}}}", sentinel)
    # Double all remaining braces so format() treats them as literals
    text = text.replace("{", "{{").replace("}", "}}")
    has_placeholder = sentinel in text
    text = text.replace(sentinel, "{0}")
    return text, has_placeholder


_REVIEW_TMPL = _JINJA.from_string("""\
{%- set prompt_body %}prompt: >-
            ${{ <<prompt_expr>> }}{%- endset -%}
<<header>>
name: tend-review
on:
  pull_request_target:
    types: [opened, synchronize, ready_for_review, reopened]

jobs:
  review:
    concurrency:
      group: ${{ github.workflow }}-${{ github.event.pull_request.number }}
      cancel-in-progress: true
    runs-on: ubuntu-24.04
    permissions:
<<permissions()>>
    steps:
      # GitHub only materializes refs/pull/N/merge for mergeable PRs — on
      # conflicting PRs it 404s and every downstream step cascades as skipped.
      # Probe first and fall back to /head so review always runs; on fallback
      # the review sees the PR branch in isolation rather than the post-merge
      # tree.
      - name: Resolve PR checkout ref
        id: pr_ref
        env:
          GITHUB_TOKEN: ${{ secrets.<<cfg.bot_token_secret>> }}
          PR: ${{ github.event.pull_request.number }}
        run: |
          if gh api "repos/${{ github.repository }}/git/ref/pull/$PR/merge" --silent 2>/dev/null; then
            echo "ref=refs/pull/$PR/merge" >> "$GITHUB_OUTPUT"
          else
            echo "ref=refs/pull/$PR/head" >> "$GITHUB_OUTPUT"
            echo "::notice::refs/pull/$PR/merge unavailable (likely merge conflict); falling back to /head"
          fi
<<checkout(cfg, ref='${{ steps.pr_ref.outputs.ref }}')>>
<<setup>>
<<agent_step(cfg, prompt_body, use_sticky_comment=True)>>
""")


def generate_review(cfg: Config) -> GeneratedWorkflow:
    wf = cfg.workflows.get("review", WorkflowConfig())
    raw_prompt = wf.prompt or cfg.default_prompt("review", "{pr_number}")
    format_body, needs_format = _escape_braces(raw_prompt, "pr_number")
    escaped = format_body.replace("'", "''")
    if needs_format:
        prompt_expr = f"format('{escaped}', github.event.pull_request.number)"
    else:
        prompt_expr = f"'{escaped}'"

    content = _REVIEW_TMPL.render(
        cfg=cfg,
        setup=_setup_yaml(cfg),
        prompt_expr=prompt_expr,
    )
    return GeneratedWorkflow(filename="tend-review.yaml", content=content)


