_CI_FIX_TMPL = _JINJA.from_string("""\
{%- set prompt_body %}prompt: |
            <<prompt>>
            - Run URL: ${{ github.event.workflow_run.html_url }}
            - Commit: ${{ github.event.workflow_run.head_sha }}
            - Commit message: ${{ github.event.workflow_run.head_commit.message }}{%- endset -%}
<<header>>
name: tend-ci-fix
on:
  workflow_run:
    workflows: <<watched|tojson>>
    types: [completed]
    branches: <<branches|tojson>>

jobs:
  fix-ci:
    if: <<fork_guard_prefix(cfg)>>github.event.workflow_run.conclusion == 'failure'
    runs-on: ubuntu-24.04
    permissions:
<<permissions(issues=False)>>
    steps:
<<checkout(cfg, ref=cfg.default_branch)>>
<<setup>>
<<agent_step(cfg, prompt_body)>>
""")


def generate_ci_fix(cfg: Config) -> GeneratedWorkflow:
    wf = cfg.workflows.get("ci-fix", WorkflowConfig())
    if wf.watched_workflows is None:
        raise click.ClickException(
            "ci-fix requires watched_workflows — specify the CI workflow(s) to monitor.\n\n"
            "  workflows:\n"
            "    ci-fix:\n"
            '      watched_workflows: ["ci"]'
        )
    watched = wf.watched_workflows
    branches = wf.branches if wf.branches is not None else [cfg.default_branch]
    prompt = (wf.prompt or cfg.default_prompt("ci-fix", "{run_id}")).replace(
        "{run_id}", "${{ github.event.workflow_run.id }}"
    )

    content = _CI_FIX_TMPL.render(
        cfg=cfg,
        watched=watched,
        branches=branches,
        setup=_setup_yaml(cfg),
        prompt=prompt,
    )
    return GeneratedWorkflow(filename="tend-ci-fix.yaml", content=content)


