def generate_triage(cfg: Config) -> GeneratedWorkflow:
    wf = cfg.workflows.get("triage", WorkflowConfig())
    prompt = (wf.prompt or "/tend:tend-triage {issue_number}").replace(
        "{issue_number}", "${{ github.event.issue.number }}"
    )
    bt = _bot_token(cfg)
    ct = _claude_token(cfg)
    bn = cfg.bot_name
    db = cfg.default_branch

    setup = _setup_yaml(cfg)
    perms = _permissions()

    content = f"""\
{HEADER}
name: tend-triage
on:
  issues:
    types: [opened]

concurrency:
  group: ${{{{ github.workflow }}}}-${{{{ github.event.issue.number }}}}
  cancel-in-progress: true

jobs:
  triage:
    if: >-
      github.event.issue.user.type != 'Bot' &&
      github.event.issue.user.login != '{bn}'
    runs-on: ubuntu-24.04
    timeout-minutes: 60
    permissions:
{perms}
    steps:
      - uses: actions/checkout@v6
        with:
          ref: {db}
          fetch-depth: 0
          fetch-tags: true
          token: {bt}
{setup}
      - uses: max-sixty/tend@v1
        with:
          github_token: {bt}
          claude_code_oauth_token: {ct}
          bot_name: {bn}
          prompt: |
            {prompt}
"""
    return GeneratedWorkflow(filename="tend-triage.yaml", content=content)


