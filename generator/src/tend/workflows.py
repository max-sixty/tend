"""Workflow definitions and YAML rendering."""

from __future__ import annotations

import copy
import io
import textwrap
from collections.abc import Callable
from dataclasses import dataclass
from importlib.metadata import version

import click
from jinja2 import Environment, PackageLoader, StrictUndefined
from jinja2.runtime import Macro
from ruamel.yaml import YAML

from tend.config import Config, WorkflowConfig

# Variable delimiters are swapped from `{{`/`}}` to `<<`/`>>` so GitHub
# Actions expressions (`${{ github.foo }}`, ubiquitous in generated YAML)
# pass through templates unchanged. Block tags `{% if %} ... {% endif %}`
# stay default — GHA never uses that form. trim_blocks + lstrip_blocks let
# control-flow tags be indented without polluting output. Templates live as
# `.yaml.j2` files under `templates/`; the `.yaml.` infix gives editors YAML
# syntax highlighting since most strip the `.j2` and apply by tail.
_JINJA = Environment(
    loader=PackageLoader("tend", "templates"),
    autoescape=False,
    trim_blocks=True,
    lstrip_blocks=True,
    undefined=StrictUndefined,
    keep_trailing_newline=True,
    variable_start_string="<<",
    variable_end_string=">>",
)

# ruamel.yaml defaults to YAML 1.2, where `on`/`yes`/`no`/`off` are strings,
# not booleans — sidesteps PyYAML's `on:` → True trap and the Norway problem.
# typ="rt" preserves insertion order and per-scalar style (matters for diff
# stability on regen); typ="safe" would sort keys alphabetically.
_YAML_BLOCK = YAML(typ="rt", pure=True)
_YAML_BLOCK.default_flow_style = False
_YAML_BLOCK.width = 200
_YAML_BLOCK.allow_unicode = True
# Round-trip explicit quoting on scalars (e.g. `cron: "17 6 * * *"` stays
# quoted). Without this, ruamel drops quotes on any string that's safe to
# unquote under YAML 1.2 — a meaningless style churn on regen.
_YAML_BLOCK.preserve_quotes = True
# Match the hand-rendered templates' 6-space `- uses:` under `steps:` (the
# `-` lives at parent-column + offset, the content at parent-column +
# sequence). Without this, `_apply_extras`-applied workflows reformat every
# step block (4→6 column for `-`, 4→8 for content) on regen — a churn diff
# for any consumer with `workflow_extra` or per-job overrides.
_YAML_BLOCK.indent(mapping=2, sequence=4, offset=2)

# Stamping the generator's own version into the header gives downstream
# nightly regen a stable anchor for detecting tend version bumps (see
# plugins/tend-ci-runner/skills/nightly/SKILL.md, Step 6) regardless of what
# templates pin for `uvx tend@...` or the action tag.
_TEND_VERSION = version("tend")
HEADER = f"""\
# Generated by tend {_TEND_VERSION}. Regenerate with: uvx tend@latest init
#
# Do not edit this file directly — it will be overwritten on regeneration.
# To customize behavior, edit the relevant skill (for example,
# `running-tend`) in this repo's .claude/skills/ directory, or open an issue at
# https://github.com/max-sixty/tend/issues for changes that need to
# happen upstream in the tend-ci-runner plugin.
"""

# Available to every template without being passed to render().
_JINJA.globals["header"] = HEADER
_JINJA.globals["tend_version"] = _TEND_VERSION


# Register every macro defined in `macros.yaml.j2` as a Jinja global so
# workflow templates can call `agent_step(...)` directly, without an
# explicit `{% from 'macros.yaml.j2' import ... %}` header in each file.
_MACROS = _JINJA.get_template("macros.yaml.j2")
_JINJA.globals.update(
    {name: val for name, val in vars(_MACROS.module).items() if isinstance(val, Macro)}
)


# Step keys are emitted in this order: step kind (uses/run) first, then
# metadata, then nested tables. Matches the hand-rendered template style;
# adopters' YAML order is otherwise preserved as-is.
_STEP_FIELD_ORDER = [
    "uses",
    "run",
    "name",
    "id",
    "if",
    "shell",
    "working-directory",
    "continue-on-error",
    "timeout-minutes",
    "with",
    "env",
]


def _setup_yaml(cfg: Config, condition: str = "") -> str:
    """Render setup steps as YAML at column 6.

    Returns empty string when no steps, or newline-prefixed block when present,
    so templates can write `<<setup>>` without extra blank lines.

    When *condition* is set, steps without an explicit `if:` receive one so
    they only run when the pre-check found work. Steps that already specify
    `if:` are left alone (with a warning) — their condition wins.
    """
    if not cfg.setup:
        return ""
    steps = []
    for step in cfg.setup:
        fields = dict(step.fields)
        if condition:
            if "if" in fields:
                click.echo(
                    "Warning: setup step has an explicit `if:`; the "
                    "notifications pre-check guard will not be added. "
                    "The step runs based on your condition alone.",
                    err=True,
                )
            else:
                fields["if"] = condition
        ordered = {k: fields[k] for k in _STEP_FIELD_ORDER if k in fields}
        for k, v in fields.items():
            ordered.setdefault(k, v)
        steps.append(ordered)
    buf = io.StringIO()
    _YAML_BLOCK.dump(steps, buf)
    return "\n" + textwrap.indent(buf.getvalue().rstrip(), "    ") + "\n"


@dataclass
class GeneratedWorkflow:
    filename: str
    content: str


