# ruamel.yaml defaults to YAML 1.2, where `on`/`yes`/`no`/`off` are strings,
# not booleans — sidesteps PyYAML's `on:` → True trap and the Norway problem.
# typ="rt" preserves insertion order and per-scalar style (matters for diff
# stability on regen); typ="safe" would sort keys alphabetically.
_YAML_BLOCK = YAML(typ="rt", pure=True)
_YAML_BLOCK.default_flow_style = False
_YAML_BLOCK.width = 200
_YAML_BLOCK.allow_unicode = True
# Round-trip explicit quoting on scalars (e.g. `cron: "17 6 * * *"` stays
# quoted). Without this, ruamel drops quotes on any string that's safe to
# unquote under YAML 1.2 — a meaningless style churn on regen.
_YAML_BLOCK.preserve_quotes = True
# Match the hand-rendered templates' 6-space `- uses:` under `steps:` (the
# `-` lives at parent-column + offset, the content at parent-column +
# sequence). Without this, `_apply_extras`-applied workflows reformat every
# step block (4→6 column for `-`, 4→8 for content) on regen — a churn diff
# for any consumer with `workflow_extra` or per-job overrides.
_YAML_BLOCK.indent(mapping=2, sequence=4, offset=2)

_YAML_FLOW = YAML(typ="safe", pure=True)
_YAML_FLOW.default_flow_style = True
_YAML_FLOW.allow_unicode = True
_YAML_FLOW.width = 10**9  # one-line scalars; never wrap

# Stamping the generator's own version into the header gives downstream
# nightly regen a stable anchor for detecting tend version bumps (see
# plugins/tend-ci-runner/skills/nightly/SKILL.md, Step 6) regardless of what
# templates pin for `uvx tend@...` or the action tag.
HEADER = f"""\
# Generated by tend {version("tend")}. Regenerate with: uvx tend@latest init
#
# Do not edit this file directly — it will be overwritten on regeneration.
# To customize behavior, edit the relevant skill (for example,
# `running-tend`) in this repo's .claude/skills/ directory, or open an issue at
# https://github.com/max-sixty/tend/issues for changes that need to
# happen upstream in the tend-ci-runner plugin.
"""


def _bot_token(cfg: Config) -> str:
    return f"${{{{ secrets.{cfg.bot_token_secret} }}}}"


def _claude_token(cfg: Config) -> str:
    return f"${{{{ secrets.{cfg.claude_token_secret} }}}}"


def _openai_key(cfg: Config) -> str:
    return f"${{{{ secrets.{cfg.openai_key_secret} }}}}"


def _codex_auth_json(cfg: Config) -> str:
    return f"${{{{ secrets.{cfg.codex_auth_json_secret} }}}}"


def _default_prompt(cfg: Config, skill: str, args_template: str = "") -> str:
    """Default prompt invoking a tend-ci-runner skill in engine-native syntax.

    Claude resolves `/tend-ci-runner:NAME` as a slash command. Codex resolves
    `$NAME` as a skill mention (or matches by description); the
    `tend-ci-runner` namespace prefix isn't needed at the prompt site because
    skill names within the plugin are unique. `args_template` is appended
    raw so callers can splice their own placeholders (`{pr_number}`,
    `{issue_number}`, etc.) and run the existing replace step.
    """
    invocation = f"/tend-ci-runner:{skill}" if cfg.engine == "claude" else f"${skill}"
    return f"{invocation} {args_template}".rstrip()


def _action_ref(cfg: Config) -> str:
    """Ref string for the engine-specific composite action.

    Both actions ship from this repo; the Codex variant lives under /codex/.
    Pinned to v1; release tags promote both action paths together.
    """
    return "max-sixty/tend@v1" if cfg.engine == "claude" else "max-sixty/tend/codex@v1"


def _agent_step(
    cfg: Config,
    prompt_body: str,
    *,
    indent: int = 6,
    use_sticky_comment: bool = False,
    if_condition: str = "",
) -> str:
    """Render the `uses: max-sixty/tend@v1` step.

    `prompt_body` is the rendered prompt value (everything after `prompt:`),
    including any leading whitespace. Callers handle the prompt's YAML shape
    (block scalar `|`, folded `>-`, expression-quoted single line) — this
    helper just splices it under `with:`.
    """
    pad = " " * indent
    bt = _bot_token(cfg)
    bn = cfg.bot_name
    ref = _action_ref(cfg)

    lines = [f"{pad}- uses: {ref}"]
    if if_condition:
        lines.append(f"{pad}  if: {if_condition}")
    lines.append(f"{pad}  with:")
    lines.append(f"{pad}    github_token: {bt}")

    if cfg.engine == "claude":
        ct = _claude_token(cfg)
        lines.append(f"{pad}    claude_code_oauth_token: {ct}")
    else:
        # Emit both Codex auth inputs unconditionally; the codex action picks
        # auth.json over api-key when both are configured. Adopters set just
        # the one they want — the other renders as an empty string.
        lines.append(f"{pad}    openai_api_key: {_openai_key(cfg)}")
        lines.append(f"{pad}    codex_auth_json: {_codex_auth_json(cfg)}")
        if cfg.effort:
            lines.append(f"{pad}    effort: {cfg.effort}")

    lines.append(f"{pad}    bot_name: {bn}")
    lines.append(f"{pad}    model: {cfg.model}")
    if use_sticky_comment and cfg.engine == "claude":
        # Codex action posts via gh from inside skill prompts; no sticky.
        lines.append(f"{pad}    use_sticky_comment: true")
    lines.append(f"{pad}    {prompt_body}")
    return "\n".join(lines)


_STEP_FIELD_ORDER = [
    "uses",
    "run",
    "name",
    "id",
    "if",
    "shell",
    "working-directory",
    "continue-on-error",
    "timeout-minutes",
    "with",
    "env",
]
# Scalar fields where we want plain (unquoted) YAML emission, matching the
# existing style of generated workflows and letting GitHub Actions expressions
# pass through unescaped.
_PLAIN_TOP_LEVEL = {
    "uses",
    "run",
    "if",
    "name",
    "id",
    "shell",
    "working-directory",
}


def _setup_yaml(cfg: Config, indent: int = 6, condition: str = "") -> str:
    """Render setup steps as YAML, indented to `indent` spaces.

    Returns empty string when no steps, or newline-prefixed block when present,
    so templates can write `{setup}` without extra blank lines.

    When *condition* is set, steps without an explicit `if:` receive one so
    they only run when the pre-check found work. Steps that already specify
    `if:` are left alone (with a warning) — their condition wins.
    """
    lines = []
    for step in cfg.setup:
        fields = dict(step.fields)
        if condition:
            if "if" in fields:
                click.echo(
                    "Warning: setup step has an explicit `if:`; the "
                    "notifications pre-check guard will not be added. "
                    "The step runs based on your condition alone.",
                    err=True,
                )
            else:
                fields["if"] = condition
        lines.append(_render_step(_order_step_fields(fields), indent))
    if not lines:
        return ""
    return "\n" + "\n".join(lines) + "\n"


def _order_step_fields(fields: dict) -> dict:
    """Emit keys in a stable order: step kind first, then metadata, then tables."""
    ordered = {k: fields[k] for k in _STEP_FIELD_ORDER if k in fields}
    for k, v in fields.items():
        if k not in ordered:
            ordered[k] = v
    return ordered


def _render_step(fields: dict, indent: int) -> str:
    """Render a step dict as a YAML list item at the given indent column."""
    pad = " " * indent
    lines = []
    for i, (key, value) in enumerate(fields.items()):
        prefix = f"{pad}- " if i == 0 else f"{pad}  "
        if isinstance(value, dict):
            lines.append(f"{prefix}{key}:")
            for sk, sv in value.items():
                lines.append(f"{pad}    {sk}: {_yaml_scalar(sv)}")
        elif key in _PLAIN_TOP_LEVEL:
            lines.append(f"{prefix}{key}: {value}")
        else:
            lines.append(f"{prefix}{key}: {_yaml_scalar(value)}")
    return "\n".join(lines)


def _yaml_scalar(value: object) -> str:
    """Render a Python scalar as a single-line YAML scalar."""
    buf = io.StringIO()
    _YAML_FLOW.dump(value, buf)
    # ruamel emits a trailing newline; strip and drop any `...` document marker.
    dumped = buf.getvalue().rstrip()
    if dumped.endswith("\n..."):
        dumped = dumped[: -len("\n...")]
    return dumped


def _fork_guard(cfg: Config) -> str:
    """`if:` expression that skips a job when running outside the canonical owner.

    Compares `github.repository_owner` against the configured owner: `secrets`
    isn't available in `jobs.<job_id>.if`, and `github.event.repository.fork`
    is null for `schedule` events, so the owner string is the only check that
    works across all the affected event types. Returns "" if the owner is
    unknown — callers omit the guard rather than fail.
    """
    if not cfg.repo_owner:
        return ""
    return f"github.repository_owner == '{_escape(cfg.repo_owner)}'"


def _permissions(issues: bool = True) -> str:
    lines = [
        "      contents: write",
        "      pull-requests: write",
        "      id-token: write",
        "      actions: read",
    ]
    if issues:
        lines.append("      issues: write")
    return "\n".join(lines)


def _escape(s: str) -> str:
    """Escape single quotes for GitHub Actions expressions."""
    return s.replace("'", "''")


@dataclass
class GeneratedWorkflow:
    filename: str
    content: str


