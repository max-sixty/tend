# Registry
# ---------------------------------------------------------------------------

GENERATORS: dict[str, Callable[[Config], GeneratedWorkflow]] = {
    "review": generate_review,
    "mention": generate_mention,
    "triage": generate_triage,
    "ci-fix": generate_ci_fix,
    "nightly": lambda cfg: _generate_scheduled(cfg, "nightly"),
    "weekly": lambda cfg: _generate_scheduled(cfg, "weekly"),
    "notifications": generate_notifications,
    "review-runs": lambda cfg: _generate_scheduled(cfg, "review-runs"),
}


def generate_all(
    cfg: Config, *, with_install_test: bool = False
) -> list[GeneratedWorkflow]:
    """Generate all enabled workflows.

    `with_install_test` opts in to the one-shot tend-install-test.yaml. The
    install-tend skill passes it during initial setup; the nightly regen
    runs without the flag so the install-test file is cleaned up once the
    install PR has landed.
    """
    results = []
    for name, gen_fn in GENERATORS.items():
        wf_cfg = cfg.workflows.get(name, WorkflowConfig())
        if not wf_cfg.enabled:
            continue
        if name == "ci-fix" and wf_cfg.watched_workflows is None:
            click.echo(
                "Skipping ci-fix: watched_workflows not configured. "
                "To enable, add to .config/tend.yaml:\n\n"
                "  workflows:\n"
                "    ci-fix:\n"
                '      watched_workflows: ["ci"]',
                err=True,
            )
            continue
        wf = gen_fn(cfg)
        wf = _apply_extras(wf, wf_cfg)
        results.append(wf)
    if with_install_test:
        wf = generate_install_test(cfg)
        wf_cfg = cfg.workflows.get("install-test", WorkflowConfig())
        wf = _apply_extras(wf, wf_cfg)
        results.append(wf)
    return results
