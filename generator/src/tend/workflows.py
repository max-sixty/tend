def generate_ci_fix(cfg: Config) -> GeneratedWorkflow:
    wf = cfg.workflows.get("ci-fix", WorkflowConfig())
    watched = wf.watched_workflows if wf.watched_workflows is not None else ["ci"]
    prompt = (wf.prompt or "/tend:tend-ci-fix {run_id}").replace(
        "{run_id}", "${{ github.event.workflow_run.id }}"
    )
    bt = _bot_token(cfg)
    ct = _claude_token(cfg)
    bn = cfg.bot_name
    db = cfg.default_branch

    setup = _setup_yaml(cfg)
    perms = _permissions(issues=False)
    watched_yaml = ", ".join(f'"{w}"' for w in watched)

    content = f"""\
{HEADER}
name: tend-ci-fix
on:
  workflow_run:
    workflows: [{watched_yaml}]
    types: [completed]
    branches: [{db}]

jobs:
  fix-ci:
    if: github.event.workflow_run.conclusion == 'failure'
    runs-on: ubuntu-24.04
    timeout-minutes: 60
    permissions:
{perms}
    steps:
      - uses: actions/checkout@v6
        with:
          ref: {db}
          fetch-depth: 0
          fetch-tags: true
          token: {bt}
{setup}
      - uses: max-sixty/tend@v1
        with:
          github_token: {bt}
          claude_code_oauth_token: {ct}
          bot_name: {bn}
          prompt: |
            {prompt}
            - Run URL: ${{{{ github.event.workflow_run.html_url }}}}
            - Commit: ${{{{ github.event.workflow_run.head_sha }}}}
            - Commit message: ${{{{ github.event.workflow_run.head_commit.message }}}}
"""
    return GeneratedWorkflow(filename="tend-ci-fix.yaml", content=content)


# ---------------------------------------------------------------------------
# Scheduled (nightly, renovate)
# ---------------------------------------------------------------------------

def _generate_scheduled(cfg: Config, name: str, default_cron: str, default_prompt: str) -> GeneratedWorkflow:
    wf = cfg.workflows.get(name, WorkflowConfig())
    cron = wf.cron or default_cron
    prompt = wf.prompt or default_prompt
    bt = _bot_token(cfg)
    ct = _claude_token(cfg)
    bn = cfg.bot_name
    db = cfg.default_branch

    setup = _setup_yaml(cfg)
    perms = _permissions()

    prompt_lines = "\n".join(f"            {line}" for line in prompt.split("\n"))
    prompt_yaml = f"prompt: |\n{prompt_lines}"

    content = f"""\
{HEADER}
name: tend-{name}
on:
  schedule:
    - cron: "{cron}"
  workflow_dispatch:

jobs:
  {name}:
    runs-on: ubuntu-24.04
    timeout-minutes: 60
    permissions:
{perms}
    steps:
      - uses: actions/checkout@v6
        with:
          ref: {db}
          fetch-depth: 0
          fetch-tags: true
          token: {bt}
{setup}
      - uses: max-sixty/tend@v1
        with:
          github_token: {bt}
          claude_code_oauth_token: {ct}
          bot_name: {bn}
          {prompt_yaml}
"""
    return GeneratedWorkflow(filename=f"tend-{name}.yaml", content=content)


def generate_nightly(cfg: Config) -> GeneratedWorkflow:
    return _generate_scheduled(cfg, "nightly", "17 6 * * *", "/tend:tend-nightly")


def generate_renovate(cfg: Config) -> GeneratedWorkflow:
    return _generate_scheduled(cfg, "renovate", "17 9 * * 0", "/tend:tend-renovate")


# ---------------------------------------------------------------------------
# Registry
# ---------------------------------------------------------------------------

GENERATORS: dict[str, Callable[[Config], GeneratedWorkflow]] = {
    "review": generate_review,
    "mention": generate_mention,
    "triage": generate_triage,
    "ci-fix": generate_ci_fix,
    "nightly": generate_nightly,
    "renovate": generate_renovate,
}


def generate_all(cfg: Config) -> list[GeneratedWorkflow]:
    """Generate all enabled workflows."""
    results = []
    for name, gen_fn in GENERATORS.items():
        wf_cfg = cfg.workflows.get(name, WorkflowConfig())
        if wf_cfg.enabled:
            results.append(gen_fn(cfg))
    return results
